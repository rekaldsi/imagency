// Aliases for backward compat
export const mockCreators = MOCK_CREATORS
export type Creator = MockCreator

export const getCreatorById = (id: string): MockCreator | undefined =>
  MOCK_CREATORS.find((c) => c.id === id)

export const searchCreators = (filters: {
  gender?: string
  voiceType?: string
  accent?: string
  useCategory?: string
  skinTone?: string
  energyArchetype?: string
  ageMin?: string
  ageMax?: string
  searchQuery?: string
}): MockCreator[] => {
  return MOCK_CREATORS.filter((creator) => {
    if (filters.gender && creator.gender !== filters.gender) return false
    if (filters.voiceType && creator.voiceType !== filters.voiceType) return false
    if (filters.accent && creator.accent !== filters.accent) return false
    if (filters.useCategory && !creator.availableUses.includes(filters.useCategory)) return false
    if (filters.skinTone && creator.skinTone !== filters.skinTone) return false
    if (filters.ageMin && creator.age < parseInt(filters.ageMin)) return false
    if (filters.ageMax && creator.age > parseInt(filters.ageMax)) return false
    if (filters.searchQuery) {
      const q = filters.searchQuery.toLowerCase()
      const name = `${creator.firstName} ${creator.lastInitial}`.toLowerCase()
      if (!name.includes(q) && !creator.id.toLowerCase().includes(q)) return false
    }
    return true
  })
}
